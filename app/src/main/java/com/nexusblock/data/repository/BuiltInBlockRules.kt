object BuiltInBlockRules {

    fun emergencyRules(): List<BlockedDomain> {
        val plainDomains = domains.distinct()
            .map { BlockedDomain(host = it, source = "builtin_emergency") }
        val wildcardRules = wildcards.map { rule ->
            BlockedDomain(host = rule, source = "builtin_emergency")
        }
        val regexRules = regexPatterns.map { pattern ->
            BlockedDomain(
                host = "/$pattern/",
                source = "builtin_emergency",
                isRegex = true,
                regexPattern = pattern
            )
        }
        return plainDomains + wildcardRules + regexRules
    }

